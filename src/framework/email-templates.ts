/**
 * Generate a single email template for a given category and position.
 */
export function generateEmailTemplate(
  categoryCode: string,
  position: string
): EmailTemplate | null {
  const cat = CATEGORIES[categoryCode as keyof typeof CATEGORIES];
  if (!cat) return null;

  const generator = POSITION_GENERATORS[position];
  if (!generator) return null;

  const templateName = buildAssetName("template", categoryCode, "Nurture", position);
  const result = generator(categoryCode, cat.name);

  return {
    templateName,
    ...result,
  };
}

/**
 * Generate all 11 nurture email templates for a category.
 */
export function generateAllTemplatesForCategory(
  categoryCode: string
): EmailTemplate[] {
  return NURTURE_EMAIL_POSITIONS.map((pos) =>
    generateEmailTemplate(categoryCode, pos.position)
  ).filter((t): t is EmailTemplate => t !== null);
}

/**
 * Get available positions for template generation.
 */
export function getAvailablePositions(): EmailPosition[] {
  return NURTURE_EMAIL_POSITIONS;
}
