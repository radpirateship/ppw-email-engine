function getCategoryContext(code: string): {
  cat: Category;
  content: typeof CATEGORY_CONTENT[number] | undefined;
} {
  const cat = CATEGORIES[code as keyof typeof CATEGORIES];
  const content = CATEGORY_CONTENT.find((c) => c.categoryCode === code);
  return { cat, content };
}

